// Unusable
const BUTTON_RST_PIN: Pin = Pin::P0_18;

const BUTTON_PIN: Pin = Pin::P0_06;

const LED_KERNEL_PIN: Pin = Pin::P0_24;

/// Personal Area Network ID for the IEEE 802.15.4 radio
